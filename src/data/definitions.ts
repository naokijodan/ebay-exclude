// other除外オプション
export const otherOptions = ['PO Box'];

// Try to load external dataset if available (project-relative path)
try {
  const candidate = path.resolve(process.cwd(), 'data', 'excluded-countries.json');
  if (fs.existsSync(candidate)) {
    const raw = JSON.parse(fs.readFileSync(candidate, 'utf8')) as any;
    if (raw && typeof raw === 'object') {
      if (raw.countryToIso && typeof raw.countryToIso === 'object') {
        countryToIso = { ...countryToIso, ...raw.countryToIso };
      }
      if (raw.regionCountries && typeof raw.regionCountries === 'object') {
        regionCountries = { ...regionCountries, ...raw.regionCountries };
      }
      // rebuild reverse map
      isoToCountry = Object.fromEntries(Object.entries(countryToIso).map(([k, v]) => [v, k]));
    }
  }
} catch {
  // ignore optional dataset errors
}
